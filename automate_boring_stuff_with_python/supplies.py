# A list of supplies
supplies = ['pens', 'staplers', 'flame-throwers', 'binders']
for i in range(len(supplies)):
    print('Index ' + str(i) + ' in supplies is: ' + supplies[i])

# len(supplies) -> 4, has 4 values
# range(len(supplies)) -> range (0, 4), 4 indexes in the list that
# starts at 0 and ends 3
